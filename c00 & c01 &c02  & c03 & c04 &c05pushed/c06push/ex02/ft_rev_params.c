#include <unistd.h>

int	main(int ac, char **av)
{
	char	*tmp;

	while (--ac > 0 && *av)
	{
		tmp = *(av + ac);
		while (*tmp)
			write(1, tmp++, 1);
		write(1, "\n", 1);
	}
	return (0);
}
