int	ft_sqrt(int nb)
{
	int	i;

	i = (nb % 10) % 2;
	while (i <= 46340)
	{
		if (i * i == nb)
			return (i);
		if (i > nb / 2)
			break ;
		i += 2;
	}
	return (0);
}
